def calc_kpi(rows: List[Dict]) -> dict:
    total_cost = sum(r.get("cost", 0) for r in rows)
    total_imp = sum(r.get("impressions", 0) for r in rows)
    total_clicks = sum(r.get("clicks", 0) for r in rows)
    total_inq = sum(r.get("inquiries", 0) for r in rows)
    total_reg = sum(r.get("regulars", 0) for r in rows)
    total_coup = sum(r.get("coupons", 0) for r in rows)

    # 기본 지표
    ctr = (total_clicks / total_imp * 100) if total_imp > 0 else 0.0
    cpc = (total_cost / total_clicks) if total_clicks > 0 else 0.0
    cpa = (total_cost / total_inq) if total_inq > 0 else 0.0

    # 확장 비용 지표
    cpm = (total_cost / total_imp * 1000) if total_imp > 0 else 0.0
    cpr = (total_cost / total_reg) if total_reg > 0 else 0.0       # 단골당 비용
    cp_coupon = (total_cost / total_coup) if total_coup > 0 else 0.0  # 쿠폰당 비용

    # 퍼널 전환율
    cvr_click_inquiry = (total_inq / total_clicks * 100) if total_clicks > 0 else 0.0
    cvr_click_regular = (total_reg / total_clicks * 100) if total_clicks > 0 else 0.0
    cvr_inquiry_regular = (total_reg / total_inq * 100) if total_inq > 0 else 0.0

    # 추가 퍼널 전환율
    cvr_inquiry_coupon = (total_coup / total_inq * 100) if total_inq > 0 else 0.0
    cvr_regular_coupon = (total_coup / total_reg * 100) if total_reg > 0 else 0.0

    # 기간별 효율 분석 (소재 ON/OFF 판단용)
    period_kpis: List[Dict] = []
    for r in rows:
        r_cost = r.get("cost", 0)
        r_clicks = r.get("clicks", 0)
        r_imp = r.get("impressions", 0)
        r_inq = r.get("inquiries", 0)
        r_reg = r.get("regulars", 0)
        r_coup = r.get("coupons", 0)
        r_ctr = (r_clicks / r_imp * 100) if r_imp > 0 else 0.0
        r_cpc = (r_cost / r_clicks) if r_clicks > 0 else 0.0
        r_cpa = (r_cost / r_inq) if r_inq > 0 else 0.0
        period_kpis.append({
            "label": r.get("period_label", ""),
            "cost": r_cost, "impressions": r_imp, "clicks": r_clicks,
            "inquiries": r_inq, "regulars": r_reg, "coupons": r_coup,
            "ctr": r_ctr, "cpc": r_cpc, "cpa": r_cpa,
        })

    # 기간별 효율 분류 (평균 CPA 기준)
    avg_cpa = cpa
    efficient_periods: List[str] = []
    inefficient_periods: List[str] = []
    for pk in period_kpis:
        if pk["cpa"] <= 0:
            continue
        if pk["cpa"] <= avg_cpa:
            efficient_periods.append(pk["label"])
        elif pk["cpa"] > avg_cpa * 1.5:
            inefficient_periods.append(pk["label"])

    # 예산 재배분 시뮬레이션
    eff_cost = sum(pk["cost"] for pk in period_kpis if pk["label"] in efficient_periods)
    eff_inq = sum(pk["inquiries"] for pk in period_kpis if pk["label"] in efficient_periods)
    ineff_cost = sum(pk["cost"] for pk in period_kpis if pk["label"] in inefficient_periods)
    eff_cpa_avg = (eff_cost / eff_inq) if eff_inq > 0 else 0.0
    realloc_extra_conversions = int(ineff_cost / eff_cpa_avg) if eff_cpa_avg > 0 else 0
    realloc_new_cpa = ((eff_cost + ineff_cost) / (eff_inq + realloc_extra_conversions)) if (eff_inq + realloc_extra_conversions) > 0 else 0.0
    realloc_cpa_improvement = ((avg_cpa - realloc_new_cpa) / avg_cpa * 100) if avg_cpa > 0 else 0.0

    return {
        "total_cost": total_cost,
        "total_impressions": total_imp,
        "total_clicks": total_clicks,
        "total_inquiries": total_inq,
        "total_regulars": total_reg,
        "total_coupons": total_coup,
        "ctr": ctr,
        "cpc": cpc,
        "cpa": cpa,
        "cpm": cpm,
        "cpr": cpr,
        "cp_coupon": cp_coupon,
        "cvr_click_inquiry": cvr_click_inquiry,
        "cvr_click_regular": cvr_click_regular,
        "cvr_inquiry_regular": cvr_inquiry_regular,
        # 추가 퍼널 전환율
        "cvr_inquiry_coupon": cvr_inquiry_coupon,
        "cvr_regular_coupon": cvr_regular_coupon,
        # 기간별 효율 분석
        "period_kpis": period_kpis,
        "efficient_periods": efficient_periods,
        "inefficient_periods": inefficient_periods,
        # 예산 재배분 시뮬레이션
        "realloc_extra_conversions": realloc_extra_conversions,
        "realloc_new_cpa": realloc_new_cpa,
        "realloc_cpa_improvement": realloc_cpa_improvement,
    }


