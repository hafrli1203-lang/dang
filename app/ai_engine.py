def parse_strategy_sections(content: str) -> dict:
    """Parse strategy analysis content into 4 sections.

    Returns dict with keys: target, competition, direction, campaign_group, raw.
    """
    section_map = {
        "타겟": "target",
        "경쟁": "competition",
        "전략": "direction",
        "캠페인": "campaign_group",
    }
    result: Dict[str, str] = {
        "target": "",
        "competition": "",
        "direction": "",
        "campaign_group": "",
        "raw": content,
    }

    parts = _re.split(r"(?m)^(## .+)", content)
    for i, part in enumerate(parts):
        if not part.startswith("## "):
            continue
        body = parts[i + 1].strip() if i + 1 < len(parts) else ""
        for keyword, key in section_map.items():
            if keyword in part:
                result[key] = body
                break

    return result


